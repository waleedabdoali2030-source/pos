export function getLocalizedField(
  item: any,
  field: string,
  lang: Language
): string {
  if (!item) return "";
  const specificField = `${field}${lang.charAt(0).toUpperCase() + lang.slice(1)}`; // e.g. nameAr or nameBn or storeNameAr
  if (item[specificField] !== undefined && item[specificField] !== "") {
    return item[specificField];
  }
  return item[field] || "";
}
